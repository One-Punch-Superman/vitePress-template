<template>
  <div class="container">
    <div class="left">
      <div class="list">
        <div v-for="(item, index) in list" :key="index" :class="{ active: item == selItem }">
          <span @click="handleItemClick(item)">{{ item.name }}</span>
        </div>
      </div>
    </div>
    <div class="content">
      <MdShow v-if="text" ref="mdShowRef" :text="text"></MdShow>
    </div>
  </div>
</template>

<script lang="ts" setup>
import MdShow from '@/components/mdShow/index.vue';
import { getVueRouterMd } from '@/api/md';
import { getVueRouterList } from '@/utils/tool';

const route = useRoute();
const router = useRouter();
const list = ref<any>([]);
const selItem = ref();
const mdShowRef = ref();
const text = ref('');

onMounted(() => {
  debugger;
  const id = Number(route.params.id);
  list.value = getVueRouterList();
  if (id) {
    selItem.value = list.value[id];
  } else {
    selItem.value = list.value[0];
    router.replace(`/vueRouter/${selItem.value.id}`);
  }
  const name = `${selItem.value.id}.${selItem.value.name}`;
  getVueRouterMd(name).then((res) => {
    text.value = res.data;
  });
});

const handleItemClick = (item: any) => {
  selItem.value = item;
  const name = `${selItem.value.id}.${selItem.value.name}`;
  getVueRouterMd(name).then((res) => {
    text.value = res.data;
    nextTick(() => {
      mdShowRef.value.init();
    });
    router.push(`/vue/${item.id}`);
  });
};
</script>

